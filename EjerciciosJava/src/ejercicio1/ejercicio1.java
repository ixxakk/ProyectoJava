package ejercicio1;

/**
 * Hacer un pseudocodigo que imprima los numeros del 1 al 100.
 * @author isaac
 * @version 1.0
 */
public class ejercicio1 {
    public static void main(String[]args){
        System.out.println("numeros del 1 al 100");
        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }
    }
    
}
